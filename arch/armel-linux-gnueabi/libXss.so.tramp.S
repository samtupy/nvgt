  .globl XScreenSaverAllocInfo
  .p2align 4
  .type XScreenSaverAllocInfo, %function
#ifndef IMPLIB_EXPORT_SHIMS
  .hidden XScreenSaverAllocInfo
#endif
XScreenSaverAllocInfo:
  .cfi_startproc

1:
  // Load address
  // TODO: can we do this faster on newer ARMs?
  ldr ip, 3f
2:
  add ip, pc, ip
  ldr ip, [ip]

  cmp ip, #0

  // Fast path
  bxne ip

  // Slow path
  ldr ip, =0
  push {ip}
  .cfi_adjust_cfa_offset 4
  PUSH_REG(lr)
  bl _libXss_so_save_regs_and_resolve
  POP_REG(lr)
  add sp, #4
  .cfi_adjust_cfa_offset -4
  bx ip

  // Force constant pool for ldr above
  .ltorg

  .cfi_endproc

3:
  .word _libXss_so_tramp_table - (2b + 8) + 0

