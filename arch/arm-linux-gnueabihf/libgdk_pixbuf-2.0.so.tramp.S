  .globl gdk_colorspace_get_type
  .p2align 4
  .type gdk_colorspace_get_type, %function
#ifndef IMPLIB_EXPORT_SHIMS
  .hidden gdk_colorspace_get_type
#endif
gdk_colorspace_get_type:
  .cfi_startproc

1:
  // Load address
  // TODO: can we do this faster on newer ARMs?
  ldr ip, 3f
2:
  add ip, pc, ip
  ldr ip, [ip]

  cmp ip, #0

  // Fast path
  bxne ip

  // Slow path
  ldr ip, =0
  push {ip}
  .cfi_adjust_cfa_offset 4
  PUSH_REG(lr)
  bl _libgdk_pixbuf_2_0_so_save_regs_and_resolve
  POP_REG(lr)
  add sp, #4
  .cfi_adjust_cfa_offset -4
  bx ip

  // Force constant pool for ldr above
  .ltorg

  .cfi_endproc

3:
  .word _libgdk_pixbuf_2_0_so_tramp_table - (2b + 8) + 0

